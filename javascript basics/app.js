//Switch
// dice values: 1-6

const dice = 5;

switch (dice) {
  case 1:
    console.log("seu numero eh 1");
    break;
  case 2:
    console.log("seu numero eh 2");
    break;
  case 3:
    console.log("seu numero eh 3");
    break;
  case 4:
    console.log("seu numero eh 4");
    break;
  case 5:
    console.log("seu numero eh 5");
    break;
  case 6:
    console.log("seu numero eh 6");
    break;
  default:
    console.log("numero nao achado");
}
