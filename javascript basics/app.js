// LET VS CONST VS VAR

// using var
var value = "some value";
value = "some other value";

// using let
let name1 = "john";
name1 = "peter";

// using const (CONSTANT) - can't re-assign
const lastName = "smith";

console.log(value);
console.log(name1);
console.log(lastName);

console.log(name1, lastName);
